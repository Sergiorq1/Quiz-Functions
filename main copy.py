def area(width, height):
    result = width * height
    return result
result = area(5, 6)
print(result)
print(area(10,26))
print(area(10,10))

def subtract(num1, num2):
    result = num1 - num2
    return result
print(subtract(12,4))
print(subtract(100,98))

def divide(num1, num2):
    result = num1 / num2
    return result

def add(num1,num2):
    result = num1 + num2
    return result
print(add(50,20))
print(add(300,40))